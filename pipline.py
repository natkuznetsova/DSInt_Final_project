def main():
    print('Sber_AP Prediction Pipeline')

    data = pd.read_csv('data/ga_sessions_target.csv', low_memory=False).drop(['session_id', 'client_id'], axis=1)

    X = data.drop(['target'], axis=1)
    y = data['target']

    numerical_transformer = Pipeline(steps=[
        ('imputer', SimpleImputer(strategy='median')),
        ('scaler', StandardScaler())
    ])

    categorical_transformer = Pipeline(steps=[
        ('imputer', SimpleImputer(strategy='most_frequent')),
        ('encoder', OneHotEncoder(handle_unknown='ignore'))
    ])

    column_transformer = ColumnTransformer(transformers=[
        ('numerical', numerical_transformer, make_column_selector(dtype_include=['int32', 'int64', 'float64'])),
        ('categorical', categorical_transformer, make_column_selector(dtype_include=['object']))
    ])

    preprocessor = Pipeline(steps=[
        ('new_features', FunctionTransformer(new_features)),
        ('del_rare', FunctionTransformer(del_rare)),
        ('filter_data', FunctionTransformer(filter_data))
    ])

    models = (
        #RandomForestClassifier(),
        LogisticRegression(random_state=42, max_iter=600, solver='liblinear', class_weight='balanced'),
        MLPClassifier(random_state=42, max_iter=500),
    )

    best_recall = .0
    best_roc_auc = .0
    best_pipe = None
    for model in models:
        pipe = Pipeline(steps=[
            ('preprocessor', preprocessor),
            ('column_transformer', column_transformer),
            ('classifier', model)
        ])

        score = cross_validate(pipe, X, y, cv=3, scoring=["roc_auc", 'recall'])
        print(f'model: {type(model).__name__}, roc_auc_mean: {score["test_roc_auc"].mean():.4f}, recall_mean:{score["test_recall"].mean():.4f}')

        if score['test_roc_auc'].mean() >= 0.65:
            if score["test_recall"].mean() > best_recall:
                best_recall = score['test_recall'].mean()
                best_roc_auc = score["test_roc_auc"].mean()
                best_pipe = pipe

    best_pipe.fit(X, y)
    print(f'best model: {type(best_pipe.named_steps["classifier"]).__name__}, roc_auc: {best_roc_auc:.4f}, recall: {best_recall:.4f}')

    with open('model/Sber_AP_pipe.pkl', 'wb') as file:
        dill.dump({
            'model': best_pipe,
            'metadata': {
                "name": "Sber AP prediction model",
                'author': 'Natalia Kuznetsova',
                "version": 1,
                'date': datetime.datetime.now(),
                'type': type(best_pipe.named_steps['classifier']).__name__,
                'roc-auc': best_roc_auc
            },
        },
            file)


